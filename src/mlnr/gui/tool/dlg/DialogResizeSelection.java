    private void jFormattedTextFieldHeightKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFormattedTextFieldHeightKeyReleased
// TODO add your handling code here:
        try {
            jFormattedTextFieldHeight.commitEdit();
        } catch (Exception e) {
            // What to do?
        }
        
        if (jCheckBoxUniform.isSelected()) {
            if (jRadioButtonMeasurement.isSelected()) {
                try {
                    float fHeight = Float.parseFloat(jFormattedTextFieldHeight.getValue().toString());
                    fHeight = fHeight * ratio_x_to_y;
                    jFormattedTextFieldWidth.setValue(fHeight);
                } catch (Exception e) {
                    // What to do?
                }
            } else
                jFormattedTextFieldWidth.setValue(Float.parseFloat(jFormattedTextFieldHeight.getValue().toString()));
        }
        
    }//GEN-LAST:event_jFormattedTextFieldHeightKeyReleased
    
    private void jFormattedTextFieldWidthKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFormattedTextFieldWidthKeyReleased
// TODO add your handling code here:
        try {
            jFormattedTextFieldWidth.commitEdit();
        } catch (Exception e) {
            System.err.println("Exception: " + e);
        }
        
        if (jCheckBoxUniform.isSelected()) {
            if (jRadioButtonMeasurement.isSelected()) {
                try {
                    float fWidth = Float.parseFloat(jFormattedTextFieldWidth.getValue().toString());
                    fWidth = fWidth * ratio_y_to_x;
                    jFormattedTextFieldHeight.setValue(fWidth);
                } catch (Exception e) {
                    System.err.println(e);
                }
            } else
                jFormattedTextFieldHeight.setValue(Float.parseFloat(jFormattedTextFieldWidth.getValue().toString()));
        }
        
    }//GEN-LAST:event_jFormattedTextFieldWidthKeyReleased
    
    private void jRadioButtonDegreeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButtonDegreeActionPerformed
        // Convert measurement to percentage.
        if (measurementOrPercentage) {
            float fWidth = Measurement.convReal(Float.parseFloat(jFormattedTextFieldWidth.getValue().toString()));
            float fHeight = Measurement.convReal(Float.parseFloat(jFormattedTextFieldHeight.getValue().toString()));
            
            // Get a percentage.
            float percentWidth = (fWidth / currWidth) * 100.0f;
            float percentHeight = (fHeight / currHeight) * 100.0f;
            
            // Now update the fields to percentages.
            jFormattedTextFieldWidth.setValue(percentWidth);
            jFormattedTextFieldHeight.setValue(percentHeight);
        }
        measurementOrPercentage = false;
        
    }//GEN-LAST:event_jRadioButtonDegreeActionPerformed
    
    private void jRadioButtonMeasurementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButtonMeasurementActionPerformed
        // Convert percentage to measurement.
        if (measurementOrPercentage == false) {
            float percentWidth = Float.parseFloat(jFormattedTextFieldWidth.getValue().toString()) / 100.0f;
            float percentHeight = Float.parseFloat(jFormattedTextFieldHeight.getValue().toString()) / 100.0f;
            
            float newWidth = currWidth * percentWidth;
            float newHeight = currHeight * percentHeight;
            
            // Now update the fields to measurements.
            jFormattedTextFieldWidth.setValue(newWidth);
            jFormattedTextFieldHeight.setValue(newHeight);
        }
        measurementOrPercentage = true;
    }//GEN-LAST:event_jRadioButtonMeasurementActionPerformed
    
