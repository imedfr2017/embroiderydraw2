    /** This will set the current size label.
     * @param width is the current size of the selected items in measurement.
     * @param height is the current size of the sekected items in measurement.
     */
    public void setCurrentSize(float width, float height) {
        currWidth = width;
        currHeight = height;
        
        float dx = currWidth;
        float dy = currHeight;
        if (dx < 0.000001f)
            dx = 0.000001f;
        if (dy < 0.000001f)
            dy = 0.000001f;
        
        ratio_x_to_y = dx / dy;
        ratio_y_to_x = dy / dx;
        
        // Set the current size of the selected vertices.
        jLabelWidth.setText("" + Measurement.convertMeasurement(currWidth, 2));
        jLabelHeight.setText("" + Measurement.convertMeasurement(currHeight, 2));
        
        // Set the resize to the current size.
        jFormattedTextFieldWidth.setValue(Measurement.convertMeasurement(currWidth, 2));
        jFormattedTextFieldHeight.setValue(Measurement.convertMeasurement(currHeight, 2));
    }
    
    /** @return The width of the new resize in measurements.
     */
    public float getWidthInMeasurements() {
        if (measurementOrPercentage)
            return Measurement.convReal(Float.parseFloat(jFormattedTextFieldWidth.getValue().toString()));
        else {
            float percentWidth = Float.parseFloat(jFormattedTextFieldWidth.getValue().toString()) / 100;
            return currWidth * percentWidth;
        }
    }
    
    /** @return The height of the new resize in measurements.
     */
    public float getHeightInMeasurements() {
        if (measurementOrPercentage)
            return Measurement.convReal(Float.parseFloat(jFormattedTextFieldHeight.getValue().toString()));
        else {
            float percentHeight = Float.parseFloat(jFormattedTextFieldHeight.getValue().toString()) / 100;
            return currHeight * percentHeight;
        }
    }
    
